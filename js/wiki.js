	function fetchPage(pageName) {
		const apiUrl = `https://en.wikipedia.org/w/api.php?action=query&format=xml&prop=extracts&titles=${encodeURIComponent(
			pageName
		)}&origin=*`;

		fetch(apiUrl)
			.then((response) => response.text())
			.then((data) => {
				const parser = new DOMParser();
				const xmlDoc = parser.parseFromString(data, "application/xml");
				displayContent(xmlDoc);
			})
			.catch((error) => console.error("Error fetching the page:", error));
	}

	function displayContent(xmlDoc) {
		let loader = document.querySelector(".loader");
        let container = document.querySelector(".container");
        
		loader.hidden = true;
        container.hidden = false;
		const pages = xmlDoc.getElementsByTagName("page");
		contentDiv.innerHTML = ""; // Clear previous content

		for (let i = 0; i < pages.length; i++) {
			const page = pages[i];
			const title = page.getAttribute("title");
			const extract =
				page.getElementsByTagName("extract")[0]?.textContent ||
				"No content available.";

			const formattedExtract = extractSections(
				extract,
				geographyVariation
			);

			contentDiv.innerHTML += `<h2>${title}</h2>`;
			contentDiv.innerHTML += formattedExtract;
		}
	}

